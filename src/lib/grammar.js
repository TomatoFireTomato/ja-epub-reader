// 在句子里识别句型，返回 [{point, detail:{explanation, example}}]（按句中出现顺序，去重）
export function detectGrammar(sentence) {
  if (!sentence) return []
  const found = []
  const seen = new Set()
  for (const p of PATTERNS) {
    if (seen.has(p.point)) continue
    const m = sentence.match(p.re)
    if (m) {
      seen.add(p.point)
      found.push({ point: p.point, index: m.index, detail: { explanation: p.zh, example: p.ex } })
    }
  }
  found.sort((a, b) => a.index - b.index)
  return found.map(({ point, detail }) => ({ point, detail }))
}
